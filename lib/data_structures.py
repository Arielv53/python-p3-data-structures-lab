def get_spicy_food_by_cuisine(spicy_foods, cuisine):
    for food in spicy_foods:
        if food["cuisine"] == cuisine:
            return food
    return None  # In case no matching cuisine is found
print(get_spicy_food_by_cuisine(spicy_foods, "American"))

